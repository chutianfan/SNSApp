package com.gitrose.mobile.model;

public class ContactsAddressInfo {
    String city;
    String cy;
    String cycode;
    String pc;
    String pro;

    public String getCity() {
        return this.city;
    }

    public void setCity(String city) {
        this.city = city;
    }

    public String getCy() {
        return this.cy;
    }

    public void setCy(String cy) {
        this.cy = cy;
    }

    public String getCycode() {
        return this.cycode;
    }

    public void setCycode(String cycode) {
        this.cycode = cycode;
    }

    public String getPc() {
        return this.pc;
    }

    public void setPc(String pc) {
        this.pc = pc;
    }

    public String getPro() {
        return this.pro;
    }

    public void setPro(String pro) {
        this.pro = pro;
    }
}
